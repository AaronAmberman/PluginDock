namespace PluginDock.Modeling
{
    /// <summary>Enumerates the possible types of visual wrappers for a plug-in.</summary>
    public enum ControlWrapper
    {
        /// <summary>Specifies to use a LayoutAnchorable for a visual wrapper.</summary>
        LayoutAnchorable = 0,

        /// <summary>Specifies to use a LayoutDocument for a visual wrapper.</summary>
        LayoutDocument = 1
    }
}